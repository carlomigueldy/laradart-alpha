class User {
  final String avatar;
  final String firstName;
  final String middleName;
  final String lastName;
  final String fullName;
  final String email;
  final String timeZone;
  final String formattedTimeZone;
  final bool isAdmin;

  User({
    this.avatar,
    this.firstName,
    this.middleName,
    this.lastName,
    this.fullName,
    this.email,
    this.timeZone,
    this.formattedTimeZone,
    this.isAdmin,
  });
}
